#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	i;
	int	len;

	i = 0;
	len = ft_strlen(s);
	while (i <= len)
	{
		if (s[len - i] == c)
			return ((char *) &(s[len - i]));
		i++;
	}
	return (NULL);
}
